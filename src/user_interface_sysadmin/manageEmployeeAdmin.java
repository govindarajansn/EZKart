              public boolean isValid(String s){
        Pattern p = Pattern.compile("^\\d{10}$");
 
        Matcher m = p.matcher(s);
 
        // Returning boolean value
        return (m.matches());
    }
    
    public boolean isValidEmail(String s){
        String regex = "^(.+)@(\\S+)$";
        return s.matches(regex);
    }
